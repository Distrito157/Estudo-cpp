#include <iostream>
using namespace std;

int main() {
  /* Inteiros (int):
  O tipo de dado "int" é usado para armazenar valores inteiros, como 1, 10, -5, etc.
  Por exemplo: */
  int idade = 25;
  int negativo = -5;
  
  /* Números de ponto flutuante (float, double):
  Os tipos de dados "float" e "double" são usados para representar números com casas decimais.
  O tipo "float" tem uma precisão menor do que o tipo "double".
  Por exemplo: */
  float altura = 1.75;
  double peso = 68.5;

  /* Caracteres (char): O tipo de dado "char" é usado para armazenar caracteres individuais,
  como 'a', 'B', '#', etc.
  Por exemplo: */
  char letra = 'A';


  /* Booleano (bool):
  O tipo de dado "bool" é usado para representar valores lógicos verdadeiro (true) ou falso (false).
  Por exemplo: */
  bool estaChovendo = true; // saida = 1
  bool mundoEstaAcabando = false; // saida = 0
  
  /* Strings (std::string):
  O tipo de dado "std::string" é usado para armazenar sequências de caracteres.
  Por exemplo: */
  std::string nome = "João";
  return 0;
}
